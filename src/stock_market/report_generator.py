# ── Logo finder ──────────────────────────────────────────────────────────────

def _find_logo() -> Optional[str]:
    """Walk up from this module to find docs/octa_bot.jpg in the project root."""
    here = os.path.dirname(os.path.abspath(__file__))
    for _ in range(6):
        candidate = os.path.join(here, "docs", "octa_bot.jpg")
        if os.path.exists(candidate):
            return candidate
        here = os.path.dirname(here)
    return None


# ── Running page header/footer callback ────────────────────────────────────────

def _make_page_cb(logo_path, symbol, generated_on, page_w, page_h, margin):
    """Return a canvas callback that draws a footer (and header on pages 2+)."""
    def cb(canvas, doc):
        from reportlab.lib.units import cm
        canvas.saveState()

        # ── Footer ──────────────────────────────────────────────────────────
        y_line = 0.95 * cm
        canvas.setStrokeColorRGB(*_TEAL)
        canvas.setLineWidth(0.4)
        canvas.line(margin, y_line, page_w - margin, y_line)

        canvas.setFont("Helvetica", 6.5)
        canvas.setFillColorRGB(*_GRAY)
        canvas.drawString(margin, y_line - 0.30 * cm,
            "Octa Bot Platform  |  For informational purposes only")
        canvas.drawCentredString(page_w / 2, y_line - 0.30 * cm,
            f"Page {doc.page}")
        canvas.drawRightString(page_w - margin, y_line - 0.30 * cm,
            f"{symbol.upper()}  |  {generated_on}")

        # ── Header on pages 2+ ───────────────────────────────────────────────
        if doc.page > 1:
            y_top = page_h - margin * 0.60
            # thin teal rule
            canvas.setStrokeColorRGB(*_TEAL)
            canvas.setLineWidth(0.4)
            canvas.line(margin, y_top - 0.05 * cm, page_w - margin, y_top - 0.05 * cm)
            # Logo thumbnail
            logo_x = margin
            if logo_path and os.path.exists(logo_path):
                icon_h = 0.55 * cm
                try:
                    canvas.drawImage(logo_path, logo_x, y_top - icon_h,
                                     width=icon_h, height=icon_h,
                                     preserveAspectRatio=True, anchor="sw")
                    logo_x += icon_h + 0.15 * cm
                except Exception:  # noqa: BLE001
                    pass
            canvas.setFont("Helvetica-Bold", 7.5)
            canvas.setFillColorRGB(*_NAVY)
            canvas.drawString(logo_x, y_top - 0.38 * cm, "Octa Bot  |  Stock Analysis Report")
            canvas.setFont("Helvetica", 7.5)
            canvas.setFillColorRGB(*_TEAL)
            canvas.drawRightString(page_w - margin, y_top - 0.38 * cm, symbol.upper())

        canvas.restoreState()
    return cb


# ── Section header helper ─────────────────────────────────────────────────────

def _section_header(title: str, styles: dict, content_w: float):
    """Return a refined section header: narrow teal accent | navy title band."""
    from reportlab.platypus import Table, TableStyle, Paragraph
    from reportlab.lib import colors

    def rgb(t):
        return colors.Color(*t)

    ACCENT_PTS = 5          # width of the left colour bar in points
    data = [["", Paragraph(title, styles["section"])]]
    t = Table(data, colWidths=[ACCENT_PTS, content_w - ACCENT_PTS])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (0, -1), rgb(_TEAL)),
        ("BACKGROUND",    (1, 0), (1, -1), rgb(_NAVY)),
        ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
        ("TOPPADDING",    (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ("LEFTPADDING",   (0, 0), (0, -1), 0),
        ("LEFTPADDING",   (1, 0), (1, -1), 10),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 8),
    ]))
    return t


