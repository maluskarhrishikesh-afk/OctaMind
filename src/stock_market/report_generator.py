"""
Stock Analysis Report Generator — PDF via reportlab
=====================================================

Produces a structured, multi-section PDF analysis report.

Inputs  : structured dicts from all stock analysis tools + LLM narrative
Output  : PDF file saved to disk, path returned

Layout:
  Cover page  — name, date, symbol, SEBI/general disclaimer
  Section 1   — Executive Summary (LLM narrative)
  Section 2   — Market Snapshot (current quote)
  Section 3   — Fundamental Analysis (Buffett metrics, moat score)
  Section 4   — Technical Analysis (RSI/MACD/Bollinger/SMA)
  Section 5   — Risk Assessment (volatility/Beta/VaR/Sharpe/MaxDD)
  Section 6   — Chart Pattern Detection
  Section 7   — News Sentiment
  Footer      — disclaimer on every page

NO buy/sell recommendations. NO price targets. Informational only.

Requires: pip install reportlab
"""
from __future__ import annotations

import os
from datetime import datetime
from typing import Any, Dict, List, Optional

# ── Reportlab imports (lazy, with clear error) ─────────────────────────────────

def _rl():
    try:
        import reportlab
        return reportlab
    except ImportError:
        raise ImportError("reportlab is not installed. Run: pip install reportlab")


