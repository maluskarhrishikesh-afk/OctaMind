# ── Helper: coloured table ─────────────────────────────────────────────────────

def _table(data: List[List[str]], col_widths, header_bg=None):
    """Build a reportlab Table with word-wrapping cells and alternating row shading."""
    from reportlab.platypus import Table, TableStyle, Paragraph
    from reportlab.lib.styles import ParagraphStyle
    from reportlab.lib import colors

    def rgb(t):
        return colors.Color(*t)

    hbg = rgb(header_bg or _TEAL2)

    # Paragraph styles used for cell text — ensures word-wrap in every column
    _cs = ParagraphStyle("_tc",  fontName="Helvetica",      fontSize=8.5, leading=12,
                         textColor=rgb(_BLACK), spaceAfter=0, spaceBefore=0)
    _hs = ParagraphStyle("_th",  fontName="Helvetica-Bold", fontSize=8.0, leading=11,
                         textColor=colors.white, spaceAfter=0, spaceBefore=0)

    wrapped = []
    for ri, row in enumerate(data):
        wr = []
        for cell in row:
            if isinstance(cell, str):
                wr.append(Paragraph(cell, _hs if ri == 0 else _cs))
            else:
                wr.append(cell)   # already a Paragraph / flowable
        wrapped.append(wr)

    style_cmds = [
        # Header row background
        ("BACKGROUND",    (0, 0), (-1, 0),  hbg),
        ("LINEBELOW",     (0, 0), (-1, 0),  1.0, rgb(_TEAL)),
        # Data rows
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, rgb(_LIGHT)]),
        # Grid
        ("LINEABOVE",     (0, 1), (-1, -1), 0.25, rgb(_DIVIDER)),
        ("LINEBEFORE",    (1, 0), (-1, -1), 0.25, rgb(_DIVIDER)),
        # Outer border
        ("BOX",           (0, 0), (-1, -1), 0.5,  rgb(_LGRAY)),
        # Padding & alignment
        ("VALIGN",        (0, 0), (-1, 0),  "MIDDLE"),   # header centred
        ("VALIGN",        (0, 1), (-1, -1), "TOP"),       # data rows top-aligned
        ("TOPPADDING",    (0, 0), (-1, -1), 5),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
        ("LEFTPADDING",   (0, 0), (-1, -1), 8),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 8),
    ]
    t = Table(wrapped, colWidths=col_widths)
    t.setStyle(TableStyle(style_cmds))
    return t


# ── Helper: score badge ────────────────────────────────────────────────────────

def _score_bar(score: float, max_score: float = 10, label: str = "") -> str:
    # Use plain ASCII characters — Unicode block elements are not in reportlab's
    # built-in Helvetica/Times fonts and render as 'n' or replacement chars.
    filled = int(round(score))
    empty  = int(max_score) - filled
    bar    = "#" * filled + "-" * empty
    return f"[{bar}]  {score}/{int(max_score)}  {label}"


# ── Null-safe formatting ────────────────────────────────────────────────────────

def _fmt(val: Any, suffix: str = "", decimals: int = 2, na: str = "N/A") -> str:
    if val is None:
        return na
    try:
        return f"{float(val):.{decimals}f}{suffix}"
    except (TypeError, ValueError):
        return str(val)


def _signed(val: Any, suffix: str = "%", decimals: int = 2) -> str:
    if val is None:
        return "N/A"
    try:
        v = float(val)
        sign = "+" if v > 0 else ""
        return f"{sign}{v:.{decimals}f}{suffix}"
    except (TypeError, ValueError):
        return str(val)


def _signal_color(text: str):
    """Return a hex colour based on signal text."""
    t = (text or "").lower()
    if any(x in t for x in ("bull", "overbought", "above", "strong", "positive", "good", "excellent", "wide")):
        return "#0a7a1e"
    if any(x in t for x in ("bear", "oversold", "below", "weak", "negative", "poor", "high risk", "no clear")):
        return "#cc1a1a"
    return "#3a3a3a"


def _rule_based_summary(
    quote:        Optional[Dict],
    technical:    Optional[Dict],
    fundamentals: Optional[Dict],
    risk:         Optional[Dict],
    sentiment:    Optional[Dict],
    patterns:     Optional[Dict],
) -> str:
    """
    Generate a concise 5-bullet executive summary from available data when an
    LLM narrative is not provided.  Each bullet is one plain-English sentence.
    """
    lines = []
    tech = technical or {}
    fund = fundamentals or {}
    rsk  = risk or {}
    sent = sentiment or {}
    pats = patterns or {}

    # 1. Trend & price position
    trend = tech.get("trend", "") or pats.get("trend", "")
    sma50  = tech.get("sma_50")
    sma200 = tech.get("sma_200")
    price  = (quote or {}).get("price")
    if trend:
        sma_clause = ""
        if sma50 and price:
            sma_clause = f", trading {'above' if price >= sma50 else 'below'} its 50-day average"
        if sma200 and price:
            sma_clause += f" and {'above' if price >= sma200 else 'below'} its 200-day average"
        lines.append(f"Stock is in a {trend} trend{sma_clause}.")

    # 2. RSI signal (rsi may be a float or a dict with {"value": ..., "signal": ...})
    _rsi_raw = tech.get("rsi")
    if isinstance(_rsi_raw, dict):
        rsi_val = _rsi_raw.get("value")
        rsi_sig = (_rsi_raw.get("signal") or "").lower()
    elif _rsi_raw is not None:
        rsi_val = float(_rsi_raw)
        rsi_sig = "overbought" if rsi_val >= 70 else ("oversold" if rsi_val <= 30 else
                  "near overbought" if rsi_val >= 65 else "near oversold" if rsi_val <= 35 else "neutral")
    else:
        rsi_val = rsi_sig = None
    if rsi_val is not None:
        if rsi_sig in ("overbought", "near overbought"):
            lines.append(f"RSI at {rsi_val:.1f} — approaching overbought territory; short-term pullback risk elevated.")
        elif rsi_sig in ("oversold", "near oversold"):
            lines.append(f"RSI at {rsi_val:.1f} — near oversold zone; potential mean-reversion opportunity.")
        else:
            lines.append(f"RSI at {rsi_val:.1f} — momentum neutral, no extreme reading.")

    # 3. Risk assessment  (supports both old and new field names)
    if rsk.get("status") == "success":
        rl  = rsk.get("risk_label") or rsk.get("risk_level") or rsk.get("overall_risk") or ""
        rs  = rsk.get("risk_score")
        vol = rsk.get("volatility") or rsk.get("annual_volatility_pct")
        dd  = rsk.get("max_drawdown") or rsk.get("max_drawdown_pct")
        rs_str  = f" ({rs}/10)" if rs is not None else ""
        vol_str = f"; annual volatility {float(vol):.1f}%" if vol is not None else ""
        dd_str  = f", max drawdown {float(dd):.1f}%" if dd is not None else ""
        if rl:
            lines.append(f"Risk profile: {rl}{rs_str}{vol_str}{dd_str}.")

    # 4. Quality / fundamentals
    if fund.get("status") == "success":
        qs = fund.get("quality_score")
        ql = fund.get("quality_label", "")
        de = fund.get("debt_to_equity")
        eg = fund.get("earnings_growth_5yr") or fund.get("earnings_growth_yoy_pct")
        rv = fund.get("revenue_growth_yoy_pct")
        # Use gross_margin_pct (correct key); fall back to operating_margin_pct with proper label
        gm = fund.get("gross_margin_pct")
        gm_label = "gross margin"
        if gm is None:
            gm = fund.get("operating_margin_pct")
            gm_label = "operating margin"
        notes = []
        if gm is not None:
            notes.append(f"{gm_label} {float(gm):.1f}%")
        if de is not None:
            notes.append(f"D/E {float(de):.2f}")
        if eg is not None:
            eg_lbl = "strong" if float(eg) >= 15 else ("positive" if float(eg) >= 0 else "negative")
            notes.append(f"{eg_lbl} earnings growth")
        if rv is not None:
            notes.append(f"revenue growth {float(rv):.1f}%")
        qual_note = f" — {', '.join(notes)}" if notes else ""
        if qs is not None:
            lines.append(f"Quality score {qs}/10 ({ql}){qual_note}.")

    # 5. Sentiment
    if sent.get("status") == "success":
        overall = sent.get("overall_sentiment", "")
        score   = sent.get("aggregate_score") or sent.get("sentiment_score")
        count   = (sent.get("article_count")
                   or (sent.get("positive_headlines", 0)
                       + sent.get("neutral_headlines", 0)
                       + sent.get("negative_headlines", 0)))
        if overall:
            score_str = f" (score {score:.2f})" if score is not None else ""
            count_str = f" across {count} recent articles" if count else ""
            lines.append(f"News sentiment {overall.lower()}{score_str}{count_str}.")

    if not lines:
        return ""
    return "\n".join(f"• {ln}" for ln in lines)


# ── Main report builder ────────────────────────────────────────────────────────

def build_report(
    symbol: str,
    quote: Optional[Dict]        = None,
    fundamentals: Optional[Dict] = None,
    technical: Optional[Dict]    = None,
    risk: Optional[Dict]         = None,
    patterns: Optional[Dict]     = None,
    sentiment: Optional[Dict]    = None,
    narrative: str               = "",
    output_dir: str              = "data",
) -> str:
    """
    Generate a PDF analysis report and save it to output_dir.

    Args:
        symbol:      Ticker symbol (used in filename).
        quote:       Result from get_quote().
        fundamentals:Result from fundamental_analysis().
        technical:   Result from technical_analysis().
        risk:        Result from risk_score().
        patterns:    Result from pattern_detection().
        sentiment:   Result from sentiment_analysis().
        narrative:   LLM-generated plain-language summary.
        output_dir:  Directory to save the PDF (created if needed).

    Returns:
        Absolute path to the generated PDF file.
    """
    _rl()  # ensure reportlab is installed

    from reportlab.lib.pagesizes import A4
    from reportlab.lib.units import cm
    from reportlab.lib import colors
    from reportlab.platypus import (
        SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
        HRFlowable, PageBreak,
    )

    os.makedirs(output_dir, exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    filename  = f"analysis_{symbol.upper()}_{timestamp}.pdf"
    filepath  = os.path.join(output_dir, filename)

    PAGE_W, PAGE_H = A4
    MARGIN = 1.8 * cm
    CONTENT_W = PAGE_W - 2 * MARGIN
    generated_on = datetime.now().strftime("%d %b %Y  %H:%M")
    logo_path = _find_logo()

    doc = SimpleDocTemplate(
        filepath,
        pagesize=A4,
        leftMargin=MARGIN,
        rightMargin=MARGIN,
        topMargin=2.2 * cm,   # leave room for running header on pages 2+
        bottomMargin=1.6 * cm,
        title=f"Stock Analysis — {symbol.upper()}",
        author="Octa Bot",
        subject="Stock Market Analysis Report (Informational Only)",
        creator="Octa Bot Platform",
    )

    st = _styles()

    def rgb(t):
        return colors.Color(*t)

    story = []

    # ── COVER PAGE ──────────────────────────────────────────────────────────────
    # Full-width navy banner — clean, no separate logo column
    title_para    = Paragraph("Octa Bot  Stock Analysis Report", st["title"])
    subtitle_para = Paragraph("Powered by Octa Bot Platform  ·  Informational Use Only", st["subtitle"])

    hdr_tbl = Table(
        [[[title_para, Spacer(1, 0.18*cm), subtitle_para]]],
        colWidths=[CONTENT_W]
    )
    hdr_tbl.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, -1), rgb(_NAVY)),
        ("VALIGN",        (0, 0), (-1, -1), "MIDDLE"),
        ("TOPPADDING",    (0, 0), (-1, -1), 20),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 20),
        ("LEFTPADDING",   (0, 0), (-1, -1), 16),
        ("RIGHTPADDING",  (0, 0), (-1, -1), 16),
    ]))
    story.append(hdr_tbl)
    # Teal accent rule directly under banner
    story.append(HRFlowable(width=CONTENT_W, thickness=3, color=rgb(_TEAL),
                            spaceAfter=0.38*cm, spaceBefore=0))

    # ── Company info card ──────────────────────────────────────────────
    company_name = (
        (quote or {}).get("name")
        or (fundamentals or {}).get("name")
        or symbol.upper()
    )
    sector   = (quote or fundamentals or {}).get("sector") or (fundamentals or {}).get("sector") or "N/A"
    industry = (quote or fundamentals or {}).get("industry") or (fundamentals or {}).get("industry") or "N/A"
    currency = (quote or {}).get("currency") or (fundamentals or {}).get("currency") or "INR"

    q = quote or {}
    # ── Pre-compute all cover-page metrics ─────────────────────────────────
    price_str  = _fmt(q.get("price"),           suffix=f" {currency}", decimals=2)
    chg_str    = _signed(q.get("change_pct"))
    mktcap_str = _fmt(q.get("market_cap"),      decimals=0)
    vol_str    = _fmt(q.get("volume"),           decimals=0)
    high52_str = _fmt(q.get("52w_high"),         decimals=2)
    low52_str  = _fmt(q.get("52w_low"),          decimals=2)
    pe_str     = _fmt(q.get("pe_ratio"),         decimals=2)
    eps_str    = _fmt(q.get("eps"),              decimals=2)
    div_str    = _fmt(q.get("dividend_yield"),   suffix="%", decimals=2)
    avgvol_str = _fmt(q.get("avg_volume"),       decimals=0)
    gen_full   = datetime.now().strftime("%d %b %Y  %H:%M")

    info_data = [
        ["Symbol",    symbol.upper(),  "Exchange/Currency", currency],
        ["Company",   company_name,    "Sector",            sector],
        ["Industry",  industry,        "Generated",         gen_full],
    ]
    if q:
        info_data += [
            ["Current Price", price_str,  "Day Change",   chg_str],
            ["52W High",      high52_str, "52W Low",      low52_str],
            ["Market Cap",    mktcap_str, "Volume",       vol_str],
            ["P/E Ratio",     pe_str,     "EPS (TTM)",    eps_str],
            ["Dividend Yield",div_str,    "Avg Volume",   avgvol_str],
        ]

    info_tbl = Table(info_data, colWidths=[CONTENT_W*0.20, CONTENT_W*0.30, CONTENT_W*0.22, CONTENT_W*0.28])
    # Build per-row background commands for clean alternating rows
    _row_cmds = []
